package org.groupweb.vscode;

public class Keyword {

    // attributes
    private String name;
    private double weight;

    // constructor
    public Keyword(String name, double weight) {
        this.name = name;
        this.weight = weight;
    }

    // getters
    public String getName() {
        return name;
    }

    public double getWeight() {
        return weight;
    }
}
